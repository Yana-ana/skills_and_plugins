_TEMPLATES: Dict[str, str] = {
    "work": _WORK_TEMPLATE,
    "social": _SOCIAL_TEMPLATE,
    "sales": _SALES_TEMPLATE,
    "other": _OTHER_TEMPLATE,
}


def register_template(scene_key: str, template: str) -> None:
    """注册或替换某场景的模板"""
    if not scene_key or not template:
        return
    _TEMPLATES[scene_key] = template
    logger.info(f"template registered for scene: {scene_key}")


def get_template(scene_key: str) -> str:
    """按场景获取模板；未注册则退回 other 模板"""
    return _TEMPLATES.get(scene_key) or _TEMPLATES["other"]


def list_scenes() -> Dict[str, int]:
    """列出已注册场景及模板长度（debug 用）"""
    return {k: len(v) for k, v in _TEMPLATES.items()}
